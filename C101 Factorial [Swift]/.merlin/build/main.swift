// Add your code below:
func factorial(n: Int) -> Int {
    print("Calculating factorial(\(n))...")
    if n <= 1 {
        print("Terminal case, returning 1.")
        return 1
    } else {
        let b = n * factorial(n: n-1)
        print("Non-terminal case, returning \(b).")
        return b
    }
}

print("The final result is: " + String(factorial(n: n)) + ".")
