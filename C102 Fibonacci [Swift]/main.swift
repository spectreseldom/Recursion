func fibonacci(n: Int) -> Int {
    print("Calculating fibonacci(\(n))...")
    if n <= 2 {
        print("Terminal case, returning 1.")
        return 1
    } else {
        let x = fibonacci(n: n-1) + fibonacci(n: n-2)
        print("Non-terminal case, returning \(x).")
        return x
    }
}
// Many, many returns because of 2 recursions inside of a single function.
print("The final result is: " + String(fibonacci(n: n)) + ".")
    
